/** シード／既存データの別名（正式名と同じ部位） */
const NAME_ALIASES: Record<string, string> = {
  クローズグリップベンチ: "クローズグリップベンチプレス",
  サイドレイズ: "サイドレイズ（ダンベル）",
  リアレイズ: "リアレイズ（ダンベル）",
  "ルーマニアンデッドリフト（ダンベル）": "ダンベルルーマニアンデッドリフト",
};

function buildNameToBodyPart(): Record<string, string> {
  const m: Record<string, string> = {};
  for (const line of CSV_ROWS.split("\n")) {
    const i = line.indexOf(",");
    if (i < 0) continue;
    const part = line.slice(0, i).trim();
    const name = line.slice(i + 1).trim();
    if (part && name) m[name] = part;
  }
  return m;
}

const EXERCISE_NAME_TO_BODY_PART = buildNameToBodyPart();

const OTHER_LABEL = "その他";

/**
 * 種目名から部位ラベル（胸・背中 等）。空名は「その他」、マスタ外は「その他」
 */
export function bodyPartLabelForExerciseName(name: string): string {
  const n = String(name || "").trim();
  if (!n) return OTHER_LABEL;
  const canonical = NAME_ALIASES[n] ?? n;
  return EXERCISE_NAME_TO_BODY_PART[canonical] ?? OTHER_LABEL;
}

export const TRAINING_BODY_PART_OTHER_LABEL = OTHER_LABEL;
